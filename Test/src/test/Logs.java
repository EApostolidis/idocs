package test;

import java.util.Date;

public class Logs {
	
	private Date date;
	
	private String name;
	
	private String minutesWatched;
	
	private String minutesDuration;
	
	private String type;

	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getMinutesWatched() {
		return minutesWatched;
	}

	public void setMinutesWatched(String minutesWatched) {
		this.minutesWatched = minutesWatched;
	}

	public String getMinutedDuration() {
		return minutesDuration;
	}

	public void setMinutedDuration(String minutedDuration) {
		this.minutesDuration = minutedDuration;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
	
	
	

}
